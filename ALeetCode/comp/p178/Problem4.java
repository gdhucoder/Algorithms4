  static class Solution {

    private List<Integer> stack = new ArrayList<>();

    public String largestMultipleOfThree(int[] d) {

      int len = d.length;
      if (len == 0) return "";
      if (len == 1) return d[0] != 0 && d[0] % 3 != 0 ? "" + d[0] : "" + 0;
      sort_re(d);
      boolean[] mask = new boolean[d.length];
      dfs(d, mask);
      String res = "";
      if (this.stack.size() > 0) {
        if (stack.get(0) == 0) {
          return "0";
        }
        else {
          for (int num : stack) {
            res += num;
          }
        }
      }

      return res;
    }

    List<Integer> temp = new ArrayList<>();

    private void dfs(int[] a, boolean[] mask) {
      if (checkValid(a, mask)) {

        for (int i = 0; i < a.length; i++) {
          if (mask[i]) continue;
          //          System.out.printf("%2d", a[i]);
          temp.add(a[i]);
        }
        if (stack.size() > this.stack.size()) {
          this.stack = temp;
        }
        if (stack.size() == this.stack.size()) {
          for (int i = 0; i < stack.size(); i++) {
            if (stack.get(i) > this.stack.get(i)) {
              this.stack = temp;
              break;
            }
          }
        }
        temp.clear();
        return;
      }
      for (int i = 0; i < a.length; i++) {
        if (!mask[i]) {
          mask[i] = true;
          dfs(a, mask);
          mask[i] = false;
        }
      }
    }

    private void sort_re(int[] a) {
      for (int i = 0; i < a.length; i++) {
        a[i] = -a[i];
      }
      Arrays.sort(a);
      for (int i = 0; i < a.length; i++) {
        a[i] = -a[i];
      }
    }

    private boolean checkValid(int[] digits, boolean[] mask) {
      long sum = 0;

      for (int i = 0; i < digits.length; i++) {
        if (mask[i]) continue;
        sum += digits[i];
      }
      return sum % 3 == 0;
    }
  }

